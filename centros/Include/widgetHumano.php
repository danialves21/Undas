            var contenido = $(this);


            $.ajax({
                method: "POST",
                url: "querys/getPrestacionesWidget.php",
                data: {
                    'especifico': especifico,
                    'Empresa': idEmpresa
                },
                success: function(response) {
                    contenido.attr("data-content", response);
                }
            });



